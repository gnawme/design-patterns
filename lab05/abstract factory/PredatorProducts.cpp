/*---------------------------------------------------------------------------*\
| \fn       Lion::Lion
\*---------------------------------------------------------------------------*/
Lion::Lion()
    : m_name( "De Lion" )
{}

/*---------------------------------------------------------------------------*\
| \fn       Lion::eat
\*---------------------------------------------------------------------------*/
void Lion::eat( 
    TPreyPtr    prey )
{
    std::cout
    << "\t"
    << name()
    << " eats "
    << prey->name()
    << std::endl;
}

/*---------------------------------------------------------------------------*\
| \fn       Lion::hunt
\*---------------------------------------------------------------------------*/
void Lion::hunt()
{
    std::cout
    << "\t"
    << name()
    << " stalks prey"
    << std::endl;
}

/*---------------------------------------------------------------------------*\
| \fn       Lion::mate
\*---------------------------------------------------------------------------*/
void Lion::mate( 
    TPredatorPtr    predator )
{
    std::cout
    << "\t"
    << name()
    << " mates with "
    << predator->name()
    << std::endl;

}

/*---------------------------------------------------------------------------*\
| \fn       Lion::name
\*---------------------------------------------------------------------------*/
std::string Lion::name()
{
    return std::string( m_name );
}

