/*---------------------------------------------------------------------------*\
| \fn       Bison::Bison
\*---------------------------------------------------------------------------*/
Bison::Bison()
    : m_name( "Da Bison" )
{}

/*---------------------------------------------------------------------------*\
| \fn       Bison::evade
\*---------------------------------------------------------------------------*/
void Bison::evade( 
    TPredatorPtr    predator )
{
    std::cout
    << "\t"
    << name()
    << " flees from "
    << predator->name()
    << std::endl;
}

/*---------------------------------------------------------------------------*\
| \fn       Bison::graze
\*---------------------------------------------------------------------------*/
void Bison::graze()
{
    std::cout
    << "\t"
    << name()
    << " eats grass"
    << std::endl;
}

/*---------------------------------------------------------------------------*\
| \fn       Bison::mate
\*---------------------------------------------------------------------------*/
void Bison::mate( 
    TPreyPtr    prey )
{
    std::cout
    << "\t"
    << name()
    << " mates with "
    << prey->name()
    << std::endl;

}

/*---------------------------------------------------------------------------*\
| \fn       Bison::name
\*---------------------------------------------------------------------------*/
std::string Bison::name()
{
    return std::string( m_name );
}

